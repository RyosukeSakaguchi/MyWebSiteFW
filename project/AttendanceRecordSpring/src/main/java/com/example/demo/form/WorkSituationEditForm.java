package com.example.demo.form;

public class WorkSituationEditForm {

	private int id;
	private int year;
	private int month;
	private int date;
	private int workSituationId;
	private String workStart;
	private String workEnd;
	private String breakTime;
	private String workStartBefore;
	private String workEndBefore;
	private String breakTimeBefore;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	}
	public int getWorkSituationId() {
		return workSituationId;
	}
	public void setWorkSituationId(int workSituationId) {
		this.workSituationId = workSituationId;
	}
	public String getWorkStart() {
		return workStart;
	}
	public void setWorkStart(String workStart) {
		this.workStart = workStart;
	}
	public String getWorkEnd() {
		return workEnd;
	}
	public void setWorkEnd(String workEnd) {
		this.workEnd = workEnd;
	}
	public String getBreakTime() {
		return breakTime;
	}
	public void setBreakTime(String breakTime) {
		this.breakTime = breakTime;
	}
	public String getWorkStartBefore() {
		return workStartBefore;
	}
	public void setWorkStartBefore(String workStartBefore) {
		this.workStartBefore = workStartBefore;
	}
	public String getWorkEndBefore() {
		return workEndBefore;
	}
	public void setWorkEndBefore(String workEndBefore) {
		this.workEndBefore = workEndBefore;
	}
	public String getBreakTimeBefore() {
		return breakTimeBefore;
	}
	public void setBreakTimeBefore(String breakTimeBefore) {
		this.breakTimeBefore = breakTimeBefore;
	}

}
